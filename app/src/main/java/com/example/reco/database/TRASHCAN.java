package com.example.reco.database;

public class TRASHCAN {
    public Integer trash_num;
    public String trashcan_borough;
    public String trashcan_roadname;
    public String trashcan_address;
    public String trashcan_locate;
    public String trashcan_type;
    public Integer star_score;

    public TRASHCAN() {
    }

    public Integer getTrash_num() {
        return trash_num;
    }

    public void setTrash_num(Integer trash_num) {
        this.trash_num = trash_num;
    }

    public String getTrashcan_borough() {
        return trashcan_borough;
    }

    public void setTrashcan_borough(String trashcan_borough) {
        this.trashcan_borough = trashcan_borough;
    }

    public String getTrashcan_roadname() {
        return trashcan_roadname;
    }

    public void setTrashcan_roadname(String trashcan_roadname) {
        this.trashcan_roadname = trashcan_roadname;
    }

    public String getTrashcan_address() {
        return trashcan_address;
    }

    public void setTrashcan_address(String trashcan_address) {
        this.trashcan_address = trashcan_address;
    }

    public String getTrashcan_locate() {
        return trashcan_locate;
    }

    public void setTrashcan_locate(String trashcan_locate) {
        this.trashcan_locate = trashcan_locate;
    }

    public String getTrashcan_type() {
        return trashcan_type;
    }

    public void setTrashcan_type(String trashcan_type) {
        this.trashcan_type = trashcan_type;
    }

    public Integer getStar_score() {
        return star_score;
    }

    public void setStar_score(Integer star_score) {
        this.star_score = star_score;
    }
}
